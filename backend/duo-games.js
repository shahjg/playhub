const duoRooms = new Map(); // roomCode -> duo room data

function generateDuoCode() {
  const chars = 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789';
  let code = 'D';
  for (let i = 0; i < 5; i++) code += chars.charAt(Math.floor(Math.random() * chars.length));
  if (duoRooms.has(code)) return generateDuoCode();
  return code;
}

function initDuoHandlers(io, socket) {

  // === CREATE DUO ROOM ===
  socket.on('duo-create', (data) => {
    const { gameType, playerName, pack, spice } = data;
    if (!gameType || !playerName) return socket.emit('duo-error', { message: 'Missing data' });

    const code = generateDuoCode();
    const questions = getQuestions(gameType, pack || 'classic', spice || 'clean');

    const room = {
      code,
      gameType,
      pack: pack || 'classic',
      spice: spice || 'clean',
      players: [{ id: socket.id, name: playerName, num: 1 }],
      questions,
      currentIndex: 0,
      choices: {},       // for WYR/TOT: { playerName: 'a'|'b' }
      reactions: {},     // { playerName: emoji }
      nextVotes: [],     // players who voted next
      favorites: {},     // { playerName: [indices] }
      matches: 0,        // for compatibility
      totalChoiceRounds: 0,
      started: false
    };

    duoRooms.set(code, room);
    socket.join(code);
    socket.duoRoom = code;
    socket.duoName = playerName;

    socket.emit('duo-created', { code, playerName, playerNum: 1 });
    console.log(`[DUO] ${playerName} created ${gameType} room ${code} (${pack}/${spice})`);
  });

  // === JOIN DUO ROOM ===
  socket.on('duo-join', (data) => {
    const { code, playerName } = data;
    if (!code || !playerName) return socket.emit('duo-error', { message: 'Missing data' });

    const room = duoRooms.get(code.toUpperCase());
    if (!room) return socket.emit('duo-error', { message: 'Room not found' });
    if (room.players.length >= 2) {
      // Check if reconnecting
      const existing = room.players.find(p => p.name === playerName);
      if (existing) {
        existing.id = socket.id;
        socket.join(room.code);
        socket.duoRoom = room.code;
        socket.duoName = playerName;
        socket.emit('duo-joined', {
          code: room.code, gameType: room.gameType, pack: room.pack, spice: room.spice,
          playerNum: existing.num, players: room.players.map(p => p.name),
          started: room.started, currentIndex: room.currentIndex,
          question: room.started ? room.questions[room.currentIndex] : null,
          total: room.questions.length
        });
        return;
      }
      return socket.emit('duo-error', { message: 'Room is full' });
    }

    room.players.push({ id: socket.id, name: playerName, num: 2 });
    socket.join(room.code);
    socket.duoRoom = room.code;
    socket.duoName = playerName;

    socket.emit('duo-joined', {
      code: room.code, gameType: room.gameType, pack: room.pack, spice: room.spice,
      playerNum: 2, players: room.players.map(p => p.name), started: false,
      total: room.questions.length
    });

    // Notify player 1
    io.to(room.code).emit('duo-player-joined', {
      players: room.players.map(p => p.name),
      playerCount: room.players.length
    });

    console.log(`[DUO] ${playerName} joined room ${room.code}`);
  });

  // === START GAME ===
  socket.on('duo-start', (data) => {
    const room = duoRooms.get(data.code);
    if (!room || room.started) return;
    if (room.players.length < 2) return socket.emit('duo-error', { message: 'Need 2 players' });

    room.started = true;
    room.currentIndex = 0;
    room.choices = {};
    room.reactions = {};
    room.nextVotes = [];

    io.to(room.code).emit('duo-round', {
      index: 0,
      question: room.questions[0],
      total: room.questions.length
    });

    console.log(`[DUO] Game started in ${room.code}`);
  });

  // === CHOICE (WYR / TOT) ===
  socket.on('duo-choice', (data) => {
    const room = duoRooms.get(data.code);
    if (!room || !room.started) return;
    const player = room.players.find(p => p.id === socket.id);
    if (!player) return;

    room.choices[player.name] = data.choice; // 'a' or 'b'

    // Notify other player that this player chose (without revealing)
    socket.to(room.code).emit('duo-player-chose', { playerName: player.name });

    // If both chose, reveal
    if (Object.keys(room.choices).length === 2) {
      room.totalChoiceRounds++;
      const names = room.players.map(p => p.name);
      const c1 = room.choices[names[0]];
      const c2 = room.choices[names[1]];
      if (c1 === c2) room.matches++;

      io.to(room.code).emit('duo-reveal', {
        choices: room.choices,
        match: c1 === c2,
        matches: room.matches,
        totalRounds: room.totalChoiceRounds,
        compatibility: Math.round((room.matches / room.totalChoiceRounds) * 100)
      });
    }
  });

  // === REACTION ===
  socket.on('duo-react', (data) => {
    const room = duoRooms.get(data.code);
    if (!room) return;
    const player = room.players.find(p => p.id === socket.id);
    if (!player) return;

    socket.to(room.code).emit('duo-reaction', {
      playerName: player.name,
      emoji: data.emoji
    });
  });

  // === NEXT ===
  socket.on('duo-next', (data) => {
    const room = duoRooms.get(data.code);
    if (!room || !room.started) return;
    const player = room.players.find(p => p.id === socket.id);
    if (!player) return;

    if (!room.nextVotes.includes(player.name)) {
      room.nextVotes.push(player.name);
      io.to(room.code).emit('duo-next-vote', {
        playerName: player.name,
        count: room.nextVotes.length
      });
    }

    // Any single player can advance
    room.currentIndex++;
    room.choices = {};
    room.reactions = {};
    room.nextVotes = [];

    if (room.currentIndex >= room.questions.length) {
      io.to(room.code).emit('duo-end', {
        total: room.questions.length,
        matches: room.matches,
        totalChoiceRounds: room.totalChoiceRounds,
        compatibility: room.totalChoiceRounds > 0 ? Math.round((room.matches / room.totalChoiceRounds) * 100) : null
      });
    } else {
      io.to(room.code).emit('duo-round', {
        index: room.currentIndex,
        question: room.questions[room.currentIndex],
        total: room.questions.length
      });
    }
  });

  // === FAVORITE ===
  socket.on('duo-favorite', (data) => {
    const room = duoRooms.get(data.code);
    if (!room) return;
    const player = room.players.find(p => p.id === socket.id);
    if (!player) return;
    if (!room.favorites[player.name]) room.favorites[player.name] = [];
    const idx = data.index;
    if (!room.favorites[player.name].includes(idx)) {
      room.favorites[player.name].push(idx);
    }
  });

  // === DISCONNECT ===
  socket.on('disconnect', () => {
    if (socket.duoRoom) {
      const room = duoRooms.get(socket.duoRoom);
      if (room) {
        io.to(room.code).emit('duo-player-left', { playerName: socket.duoName });
        // Clean up after 60s
        setTimeout(() => {
          const r = duoRooms.get(socket.duoRoom);
          if (r && r.players.every(p => !io.sockets.sockets.get(p.id))) {
            duoRooms.delete(socket.duoRoom);
          }
        }, 60000);
      }
    }
  });
}

